#include <stdio.h>

int main() {
    // Integer data type
    int age = 25;

    // Floating-point data type
    float height = 1.75;

    // Character data type
    char initial = 'A';

    // Printing the values
    printf("Integer (age): %d\n", age);
    printf("Float (height): %f\n", height);
    printf("Character (initial): %c\n", initial);

    return 0;
}
